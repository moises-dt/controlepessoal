    public void chamarTela(JInternalFrame p) {
        DesktopPane.add(p);
        int x = (DesktopPane.getWidth() / 2) - (p.getWidth() / 2);
        int y = (DesktopPane.getHeight() / 2) - (p.getHeight() / 2);
        p.setVisible(true);
        p.setLocation(x, y);
        p.toFront();
        p.show();
    }
    
